# Sort organizes list numerically and alphabetically

ages = [27, 60, 14, 35, 3, 76]
print(ages)

ages.sort()
print(ages)

names = ['Mei', 'John', 'Ben', 'Amisha']
print(names)

names.sort()
print(names)

stats = [[3, 2], [1, 2], [1, 1], [3, 1]]  # stats[2][1] -> 1
print(stats)

stats.sort()
print(stats)

# Functions to get the highest and lowest values and sum of a list

print(min(ages))
print(max(ages))
print(sum(ages))
